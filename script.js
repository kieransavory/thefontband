function navPageOpen() {

    let navPage = document.getElementById('navPage');
    let navButton = document.getElementById('menuToggle');

    navPage.classList.toggle('active');
    navButton.style.backgroundColor = "black";
    menuToggle.classList.toggle('active');
}

function navPageClose() {

    let navPage = document.getElementById('navPage');
    let navButton = document.getElementById('menuToggle');

    navPage.classList.toggle('active');
    navButton.style.backgroundColor = "black";
    menuToggle.classList.toggle('active');

}